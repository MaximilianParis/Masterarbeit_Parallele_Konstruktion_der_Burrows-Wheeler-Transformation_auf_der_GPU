    }
}


__global__ void Init_BWT_Sort_Prefix_Sums3(int* Rule_Cnt, int* Rule_Stack_Index, int* Prefix_Sum1, int* Prefix_Sum2, int* Rank_Group, int rules_plus_stack, int size_Rules) {
