void Prefix_Sum(unsigned int* A, unsigned int* B, unsigned int* Res, int size) {
    int num_blocks = (size - 1 + MAX_THREADS_PER_BLOCK) / (MAX_THREADS_PER_BLOCK);
    unsigned int* Prefix_Sum1 = A;
    unsigned int* Prefix_Sum2 = B;
    for (int stride = 1; stride < size; stride <<= 1) {
        //Copy_Array << <num_blocks, MAX_THREADS_PER_BLOCK >> > (B, A, size);

        Prefix_Sum_Kernel << <num_blocks, MAX_THREADS_PER_BLOCK >> > (Prefix_Sum1, Prefix_Sum2, size, stride);
        swap(Prefix_Sum1, Prefix_Sum2);
    }
    if (A != Prefix_Sum1)
        Copy_Array << <num_blocks, MAX_THREADS_PER_BLOCK >> > (A, B, size);

    Init_Res << <1, 1 >> > (Res, A, size - 1);
}





void Pointer_Jumping(int* A, int* B, int size) {
    int num_blocks = (size - 1 + MAX_THREADS_PER_BLOCK) / (MAX_THREADS_PER_BLOCK);

    for (int stride = 1; stride < size; stride <<= 1) {
        Copy_Array << <num_blocks, MAX_THREADS_PER_BLOCK >> > (B, A, size);

        Pointer_Jumping_Kernel << <num_blocks, MAX_THREADS_PER_BLOCK >> > (A, B, size, stride);
    }


}





