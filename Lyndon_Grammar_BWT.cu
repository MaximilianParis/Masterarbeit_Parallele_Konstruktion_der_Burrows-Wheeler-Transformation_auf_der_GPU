        if (Prefix_Sum1[group] == 0) {
            assert(Rule_Stack_Index[thread_id] == -1 || Rule_Stack_Index[thread_id] >= size_Rules);
            Rank_Group[thread_id] = -group - 1;
        }
    }
}

void Build_BWT() {
    int* Rule_Stack_Index;
    ull* Key;
    int* Stack_Start;
    int* Right_Parent_Key1;
    int* Right_Parent_Key2;
    int* Right_Parent;
    int* Index_To_Right_Parent_L;
    int* Index_To_Right_Parent_R;
    int* Rank_Group;
    int* BWT_Offset;
    unsigned char* Character_Key;

    int rules_plus_stack = size_Rules + size_Stack;
    int num_blocks_rules_plus_stack = (rules_plus_stack - 1 + MAX_THREADS_PER_BLOCK) / MAX_THREADS_PER_BLOCK;

    //Print_Grammar<<<1,1>>>(Rule_Start,Rule_Rigth,Rule_Left,Rule_length,Rule_Rank,Stack,Dev_Input,size_Rules,size_Stack);

    cudaMalloc((void**)&Auxiliar2, rules_plus_stack * sizeof(int));

    cudaMalloc((void**)&Index_To_Right_Parent_L, size_Rules * sizeof(int));
    cudaMalloc((void**)&Index_To_Right_Parent_R, size_Rules * sizeof(int));


    Compute_Size_right_Parent << < num_blocks_rules_plus_stack, MAX_THREADS_PER_BLOCK >> > (Rule_Rigth, Rule_Left, Stack, Auxiliar2, size_Rules, rules_plus_stack);

    thrust::inclusive_scan(
        thrust::device_pointer_cast(Auxiliar2),
        thrust::device_pointer_cast(Auxiliar2 + rules_plus_stack),
        thrust::device_pointer_cast(Auxiliar2)
    );

    int size_Right_Parent;

    cudaMemcpy(&size_Right_Parent, Auxiliar2 + rules_plus_stack - 1, sizeof(unsigned int), cudaMemcpyDeviceToHost);

    if (size_Right_Parent > 0) {

        int num_blocks_size_Right_Parent = (size_Right_Parent - 1 + MAX_THREADS_PER_BLOCK) / MAX_THREADS_PER_BLOCK;

        /// printf("\n %d", size_Right_Parent);

        cudaMalloc((void**)&Right_Parent_Key1, size_Right_Parent * sizeof(int));
        cudaMalloc((void**)&Right_Parent_Key2, size_Right_Parent * sizeof(int));

        Compute_right_Parent << < num_blocks_rules_plus_stack, MAX_THREADS_PER_BLOCK >> > (Rule_Rigth, Rule_Left, Stack, Auxiliar2, Right_Parent_Key1, Right_Parent_Key2, size_Rules, rules_plus_stack);

        thrust::sort_by_key(
            thrust::device_pointer_cast(Right_Parent_Key1),
            thrust::device_pointer_cast(Right_Parent_Key1 + size_Right_Parent),
            thrust::device_pointer_cast(Right_Parent_Key2)
        );

        thrust::stable_sort_by_key(
            thrust::device_pointer_cast(Right_Parent_Key2),
            thrust::device_pointer_cast(Right_Parent_Key2 + size_Right_Parent),
            thrust::device_pointer_cast(Right_Parent_Key1)
        );

        Compute_Index_To_Right_Parent_L_And_R << < num_blocks_size_Right_Parent, MAX_THREADS_PER_BLOCK >> > (Right_Parent_Key1, Right_Parent_Key2, Index_To_Right_Parent_L, Index_To_Right_Parent_R, size_Right_Parent);

        // printf("\n %d", size_Right_Parent);
        Right_Parent = Right_Parent_Key1;
        cudaFree(Right_Parent_Key2);
    }
    else
        cudaMalloc((void**)&Right_Parent, sizeof(int));


    cudaMalloc((void**)&Stack_Start, size_Stack * sizeof(int));

    Init_Stack_Start_Prefix_Sum << < num_blocks_Stack, MAX_THREADS_PER_BLOCK >> > (Stack_Start, Rule_length, Stack, size_Stack);

    thrust::exclusive_scan(
        thrust::device_pointer_cast(Stack_Start),
        thrust::device_pointer_cast(Stack_Start + size_Stack),
        thrust::device_pointer_cast(Stack_Start)
    );


    Init_Rule_Stack_Index_Prefix_Sum << < num_blocks_rules_plus_stack, MAX_THREADS_PER_BLOCK >> > (Rule_Left, Auxiliar2, rules_plus_stack, size_Rules);

    cudaFree(Rule_Left);

    thrust::inclusive_scan(
        thrust::device_pointer_cast(Auxiliar2),
        thrust::device_pointer_cast(Auxiliar2 + rules_plus_stack),
        thrust::device_pointer_cast(Auxiliar2)
    );

    int rules_plus_stack_New;
    cudaMemcpy(&rules_plus_stack_New, Auxiliar2 + rules_plus_stack - 1, sizeof(unsigned int), cudaMemcpyDeviceToHost);

    cudaMalloc((void**)&Rule_Stack_Index, rules_plus_stack_New * sizeof(int));
    cudaMalloc((void**)&Character_Key, rules_plus_stack_New * sizeof(unsigned char));
    cudaMalloc((void**)&BWT_Offset, rules_plus_stack_New * sizeof(int));
    cudaMalloc((void**)&Rank_Group, rules_plus_stack_New * sizeof(int));


    Init_Rule_Stack_Index_And_Rank_Group_And_BWT_Offset << < num_blocks_rules_plus_stack, MAX_THREADS_PER_BLOCK >> > (Rule_Rigth, Rule_length, Rule_Start, Rule_Stack_Index, Rank_Group, BWT_Offset, Character_Key, Dev_Input, Stack_Start, Auxiliar2, rules_plus_stack, size_Rules, n);

    rules_plus_stack = rules_plus_stack_New;
    num_blocks_rules_plus_stack = (rules_plus_stack - 1 + MAX_THREADS_PER_BLOCK) / MAX_THREADS_PER_BLOCK;

    cudaFree(Rule_length);
    cudaFree(Rule_Start);
    cudaFree(Stack_Start);

    cudaMalloc((void**)&Auxiliar1, 2 * rules_plus_stack * sizeof(int));

    Key = (ull*) Auxiliar1;
    int* Prefix_Sum1 = (int*)Auxiliar1;
    int* Prefix_Sum2 = (int*)(Auxiliar1 + rules_plus_stack);
    int* Prefix_Sum3 = (int*)Auxiliar2;
    int* Helper2 = (int*)Auxiliar2;
    int* Rule_Stack_Index_Temp = (int*)Auxiliar1;
    int* BWT_Offset_Temp = (int*)(Auxiliar1 + rules_plus_stack);
    int* Rank_Group_Temp = (int*)Auxiliar1;
    unsigned char* Character_Key_Temp = (unsigned char*)(Auxiliar1 + rules_plus_stack);
    int array_size = rules_plus_stack;
    while (rules_plus_stack > 0) {

        Init_Key_For_BWT_Sort << < num_blocks_rules_plus_stack, MAX_THREADS_PER_BLOCK >> > (Rule_Stack_Index, Rank_Group, Key, Rule_Rigth, Rule_Rank, Stack, rules_plus_stack, size_Rules, size_Stack, n);

        thrust::sort_by_key(
            thrust::device_pointer_cast(Character_Key),
            thrust::device_pointer_cast(Character_Key + rules_plus_stack),
            thrust::make_zip_iterator(thrust::make_tuple(thrust::device_pointer_cast(Key), thrust::device_pointer_cast(Rule_Stack_Index), thrust::device_pointer_cast(Rank_Group), thrust::device_pointer_cast(BWT_Offset)))
        );

        thrust::stable_sort_by_key(
            thrust::device_pointer_cast(Key),
            thrust::device_pointer_cast(Key + rules_plus_stack),
            thrust::make_zip_iterator(thrust::make_tuple(thrust::device_pointer_cast(Character_Key), thrust::device_pointer_cast(Rule_Stack_Index), thrust::device_pointer_cast(Rank_Group), thrust::device_pointer_cast(BWT_Offset)))
        );

        Init_Same_Group_Prefix_Sum << < num_blocks_rules_plus_stack, MAX_THREADS_PER_BLOCK >> > (Key, Rule_Cnt, Rule_Stack_Index, Rank_Group, rules_plus_stack, size_Rules);

        thrust::inclusive_scan(
            thrust::device_pointer_cast(Rank_Group),
            thrust::device_pointer_cast(Rank_Group + rules_plus_stack),
            thrust::device_pointer_cast(Rank_Group)
        );

        Init_Same_Group_Index1 << < num_blocks_rules_plus_stack, MAX_THREADS_PER_BLOCK >> > (Key, Rank_Group, Helper2, rules_plus_stack);

        Init_Same_Group_Index2 << < num_blocks_rules_plus_stack, MAX_THREADS_PER_BLOCK >> > (Rank_Group, Helper2, rules_plus_stack);

        Init_BWT_Sort_Prefix_Sums1 << < num_blocks_rules_plus_stack, MAX_THREADS_PER_BLOCK >> > (Key, Character_Key, Rank_Group, Helper2, rules_plus_stack, size_Rules);

        Init_BWT_Sort_Prefix_Sums2 << < num_blocks_rules_plus_stack, MAX_THREADS_PER_BLOCK >> > (Rank_Group, Helper2, rules_plus_stack, size_Rules);

        Init_BWT_Sort_Prefix_Sums3 << < num_blocks_rules_plus_stack, MAX_THREADS_PER_BLOCK >> > (Rule_Cnt, Rule_Stack_Index, Prefix_Sum1, Prefix_Sum2, Rank_Group, rules_plus_stack, size_Rules);

        thrust::inclusive_scan(
            thrust::device_pointer_cast(Prefix_Sum1),
            thrust::device_pointer_cast(Prefix_Sum1 + rules_plus_stack),
            thrust::device_pointer_cast(Prefix_Sum1)
        );

        thrust::inclusive_scan(
            thrust::device_pointer_cast(Prefix_Sum2),
            thrust::device_pointer_cast(Prefix_Sum2 + rules_plus_stack),
            thrust::device_pointer_cast(Prefix_Sum2)
        );

