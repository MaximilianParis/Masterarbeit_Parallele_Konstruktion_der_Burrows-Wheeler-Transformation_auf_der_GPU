struct rule_word {
    int index;
    const char* suff;
    int length;
};

int cmp1(const rule_word& a, const rule_word& b) {
    int minLen = std::min(a.length, b.length);
    int res = strncmp(a.suff, b.suff, minLen);

    if (res == 0) {
        // Wenn die ersten minLen Zeichen gleich sind, entscheidet die Länge
        if (a.length < b.length)
            return -1;  // a < b
        else if (a.length > b.length)
            return 1;   // a > b
        else
            return 0;   // komplett gleich
    }
    return res; // negativer Wert: a < b, positiver Wert: a > b
}

bool cmp(const rule_word& a, const rule_word& b) {
    int minLen = std::min(a.length, b.length);
    int res = strncmp(a.suff, b.suff, minLen);
    if (res == 0)
        return a.length < b.length;  // kürzerer String zuerst
    return res < 0;
}

