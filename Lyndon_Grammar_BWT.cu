template <ull p,int IDX>
__global__ void Init_Radix_Sort4(uint4* Rule_Value, int* Rule_Start, int* Rule_length, ull* Prefix_Sum, unsigned char* Dev_Input, int size_Rules, int n) {
    int thread_id = threadIdx.x + blockDim.x * blockIdx.x;
    if (thread_id < size_Rules) {
        int index = Rule_Value[thread_id].w;
        int rule_start = Rule_Start[index];
        int rule_length = Rule_length[index];


        ull prefix_sum_val1;

        int prefix_sum_index = ((rule_start + rule_length - 1) / Mod_Prefix_Sum_Divisor);
        uint8_t rest = ((rule_start + rule_length - 1) - prefix_sum_index * Mod_Prefix_Sum_Divisor);
        if (rest != Mod_Prefix_Sum_Divisor - 1) {
            prefix_sum_index--;
            prefix_sum_val1 = (prefix_sum_index >= 0) ? Prefix_Sum[prefix_sum_index] : 0;

            if (prefix_sum_index == -1) {
                ull potenz = 1;
                
                for (uint8_t i = 0; i <= rest; i++) {
                    prefix_sum_val1 = ((((ui128(potenz) * Dev_Input[i]) % p) + prefix_sum_val1) % p);
                    potenz = ((Base * ui128(potenz)) % p);
                }
            }
            else {
                ull potenz = 1;
                int potenz_num = (prefix_sum_index + 1) * Mod_Prefix_Sum_Divisor;
                int doubler = 1;
               
                uint8_t cur_two_potenz = 0;
                while (doubler <= potenz_num) {
                    potenz = ((potenz_num & doubler) > 0) ? ((ui128(potenz) * Prime_Mod_Potenz_Table[IDX][cur_two_potenz]) % p) : potenz;
                    //cur_potenz = (cur_potenz * cur_potenz) % p;
                    doubler <<= 1;
                    cur_two_potenz++;
                }
                
                for (uint8_t i = 0; i <= rest; i++) {
                    prefix_sum_val1 = ((((ui128(potenz) * Dev_Input[i + potenz_num]) % p) + prefix_sum_val1) % p);
                    potenz = ((Base * ui128(potenz)) % p);
                }
            }
        }
        else
            prefix_sum_val1 = Prefix_Sum[prefix_sum_index];

        ull prefix_sum_val2;
        prefix_sum_index = ((rule_start - 1) / Mod_Prefix_Sum_Divisor);
        rest = ((rule_start - 1) - prefix_sum_index * Mod_Prefix_Sum_Divisor);
        if (rest != Mod_Prefix_Sum_Divisor - 1 && rule_start - 1 >= 0) {
            prefix_sum_index--;
            prefix_sum_val2 = (prefix_sum_index >= 0) ? Prefix_Sum[prefix_sum_index] : 0;

            if (prefix_sum_index == -1) {
                ull potenz = 1;
              
                for (uint8_t i = 0; i <= rest; i++) {
                    prefix_sum_val2 = ((((ui128(potenz) * Dev_Input[i]) % p) + prefix_sum_val2) % p);
                    potenz = ((Base * ui128(potenz)) % p);
                }
            }
            else {
                ull potenz = 1;
                int potenz_num = (prefix_sum_index + 1) * Mod_Prefix_Sum_Divisor;
                int doubler = 1;
                uint8_t cur_two_potenz = 0;
                while (doubler <= potenz_num) {
                    potenz = ((potenz_num & doubler) > 0) ? ((ui128(potenz) * Prime_Mod_Potenz_Table[IDX][cur_two_potenz]) % p) : potenz;
                    //cur_potenz = (cur_potenz * cur_potenz) % p;
                    doubler <<= 1;
                    cur_two_potenz++;
                }

               
                for (uint8_t i = 0; i <= rest; i++) {
                    prefix_sum_val2 = ((((ui128(potenz) * Dev_Input[i + potenz_num]) % p) + prefix_sum_val2) % p);
                    potenz = ((Base * ui128(potenz)) % p);
                }
            }
        }
        else if (rule_start - 1 >= 0)
            prefix_sum_val2 = Prefix_Sum[prefix_sum_index];
        else
            prefix_sum_val2 = 0;

        ull rank2 = (((ui128(prefix_sum_val1) + p) - prefix_sum_val2) % p);
        int potenz_num = n - (rule_start + 2) + 1;
        int doubler = 1;
        uint8_t cur_two_potenz = 0;
      
        while (doubler <= potenz_num) {
            rank2 = ((potenz_num & doubler) > 0) ? ((ui128(rank2) * Prime_Mod_Potenz_Table[IDX][cur_two_potenz]) % p) : rank2;
            //cur_potenz = (cur_potenz * cur_potenz) % p;
            doubler <<= 1;
            cur_two_potenz++;
        }
        //ull rank2 = ((ui128(temp) * (((n - (rule_start + 2)) >= 0) ? Potenz[(n - (rule_start + 2))] : 1)) % p);


        Rule_Value[thread_id].x = rank2;
        Rule_Value[thread_id].y = (rank2 >> 32);
    }

}


