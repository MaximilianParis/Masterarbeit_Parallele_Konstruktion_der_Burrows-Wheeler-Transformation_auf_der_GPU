}

void Pointer_Jumping_Lyndon_Rule_Sort_With_Rank_Calc(int* A, int* B, int* C, int* D, int size, int tree_depth) {
    int num_blocks = (size - 1 + MAX_THREADS_PER_BLOCK) / (MAX_THREADS_PER_BLOCK);
    int* Jumper1 = A;
    int* Jumper2 = B;
    int* Rank1 = C;
    int* Rank2 = D;
    for (int stride = 1; stride < tree_depth + 1; stride <<= 1) {
        //Copy_Array << <num_blocks, MAX_THREADS_PER_BLOCK >> > (Jumper2, Jumper1, size);

        Pointer_Jumping_Lyndon_Rule_Sort_With_Rank_Calc_Kernel << <num_blocks, MAX_THREADS_PER_BLOCK >> > (Jumper1, Jumper2, Rank1, Rank2, size, stride);
        swap(Jumper1, Jumper2);
        swap(Rank1, Rank2);

    }

    if (A != Jumper1) {
        Copy_Array << <num_blocks, MAX_THREADS_PER_BLOCK >> > (A, B, size);
        Copy_Array << <num_blocks, MAX_THREADS_PER_BLOCK >> > (C, D, size);
    }


}






__global__ void Init_Device_Tree_Depth_Intervals(int* Device_Tree_Depth_Intervals, ull* Rule_Key, int size_Rules) {
    int thread_id = threadIdx.x + blockDim.x * blockIdx.x;
    if (thread_id < size_Rules) {
        ull key = Rule_Key[thread_id];

        int tree_depth_cur = (key >> 32);

        if (thread_id < size_Rules - 1) {

            ull key_next = Rule_Key[thread_id + 1];

            int tree_depth_next = (key_next >> 32);

            if (tree_depth_next != tree_depth_cur)
                Device_Tree_Depth_Intervals[tree_depth_cur] = thread_id;

        }
    }
}

