__global__ void Init_Rule_Index_And_Rule_Key_Pair_Rule_Left_And_Rule_Tree_Depth(int* Rule_Index, int* Rule_Left, int* Rule_Path_Length, ull* Rule_Key, int size_Rules, int tree_depth) {
