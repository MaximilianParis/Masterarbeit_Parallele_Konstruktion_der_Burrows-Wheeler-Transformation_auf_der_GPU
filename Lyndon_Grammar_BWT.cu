__global__ void Enlarge_BWT_Sort3(int* Right_Parent, int* Index_To_Right_Parent_L, int* Prefix_Sum2, int* Rule_Stack_Index, int rules_plus_stack_New, int size_Rules, int size_Stack) {
    int thread_id = threadIdx.x + blockDim.x * blockIdx.x;
    if (thread_id < rules_plus_stack_New) {


        int rule_stack_index = Rule_Stack_Index[thread_id];

        if (rule_stack_index < size_Rules) {
            int i = thread_id - Prefix_Sum2[thread_id];
            int l = Index_To_Right_Parent_L[rule_stack_index];
            i += l;

            int cur_right_parent = Right_Parent[i];
            Rule_Stack_Index[thread_id] = cur_right_parent;

        }
        else {
            int index = rule_stack_index - size_Rules;
            Rule_Stack_Index[thread_id] = (index + 1 < size_Stack) ? rule_stack_index + 1 : -1;

        }
    }
}
