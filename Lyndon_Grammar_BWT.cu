struct Rule_String_Comparator {
    unsigned char* dev_input;

    Rule_String_Comparator(unsigned char* dev_input_new) :dev_input(dev_input_new) {}

    __host__ __device__
        bool operator()(const thrust::tuple<int, int, int, ull, ull, ull>& a,
            const thrust::tuple<int, int, int, ull, ull, ull>& b) const
    {
        int rule_left1 = thrust::get<0>(a);
        int rule_start1 = thrust::get<1>(a);
        int rule_length1 = thrust::get<2>(a);
        ull rule_prefix11 = thrust::get<3>(a);
        ull rule_prefix12 = thrust::get<4>(a);
        ull rule_prefix13 = thrust::get<5>(a);

        int rule_left2 = thrust::get<0>(b);
        int rule_start2 = thrust::get<1>(b);
        int rule_length2 = thrust::get<2>(b);
        ull rule_prefix21 = thrust::get<3>(b);
        ull rule_prefix22 = thrust::get<4>(b);
        ull rule_prefix23 = thrust::get<5>(b);


        if (rule_left1 < rule_left2) return true;
        else if (rule_left1 > rule_left2) return false;
        else if (rule_left1 != 0) {

            if (rule_prefix11 < rule_prefix21) return true;
            else if (rule_prefix11 > rule_prefix21) return false;

            if (rule_length1 > 8 && rule_length2 > 8) {
                if (rule_prefix12 < rule_prefix22) return true;
                else if (rule_prefix12 > rule_prefix22) return false;

            }

            if (rule_length1 > 16 && rule_length2 > 16) {
                if (rule_prefix13 < rule_prefix23) return true;
                else if (rule_prefix13 > rule_prefix23) return false;

            }

            uint8_t cond_compare = 0;

            for (int j = 24; j < rule_length1 && j < rule_length2 && cond_compare == 0; j++) {
                unsigned char value1 = dev_input[rule_start1 + j];
                unsigned char value2 = dev_input[rule_start2 + j];

                if (value1 == value2)
                    cond_compare = 0;
                else if (value1 > value2)
                    cond_compare = 1;
                else
                    cond_compare = 2;
            }

            if (cond_compare == 0) {
                if (rule_length1 > rule_length2)
                    cond_compare = 1;
                else if (rule_length1 < rule_length2)
                    cond_compare = 2;
            }

            if (cond_compare == 2)
                return true;
            else
                return false;
        }
        else {
            if (rule_start1 < rule_start2) return true;
            else return false;
        }

    }
};


