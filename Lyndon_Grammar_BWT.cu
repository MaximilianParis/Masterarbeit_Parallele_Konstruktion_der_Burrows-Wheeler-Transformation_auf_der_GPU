__global__ void Pointer_Jumping_Lyndon_Rule_Sort_Kernel(int* Jumper1, int* Jumper2, int* Rule_Path_Length, int n, int stride) {
    int thread_id = threadIdx.x + blockDim.x * blockIdx.x;
    if (thread_id < n) {

        int jump = Jumper1[thread_id];
        if (jump != -1) {
            int next_parent = Jumper1[jump];
            Jumper2[thread_id] = next_parent;
            if (next_parent == -1) {
                Rule_Path_Length[thread_id] = stride + Rule_Path_Length[jump];
            }
        }
        else
            Jumper2[thread_id] = -1;

    }


}

void Pointer_Jumping_Lyndon_Rule_Sort(int* A, int* B, int* Rule_Path_Length, int size) {
    int num_blocks = (size - 1 + MAX_THREADS_PER_BLOCK) / (MAX_THREADS_PER_BLOCK);
    int* Jumper1 = A;
    int* Jumper2 = B;
    for (int stride = 1; stride < size; stride <<= 1) {
        //Copy_Array << <num_blocks, MAX_THREADS_PER_BLOCK >> > (Jumper2, Jumper1, size);

        Pointer_Jumping_Lyndon_Rule_Sort_Kernel << <num_blocks, MAX_THREADS_PER_BLOCK >> > (Jumper1, Jumper2, Rule_Path_Length, size, stride);
        swap(Jumper1, Jumper2);

    }

    if (A != Jumper1)
        Copy_Array << <num_blocks, MAX_THREADS_PER_BLOCK >> > (A, B, size);


}

