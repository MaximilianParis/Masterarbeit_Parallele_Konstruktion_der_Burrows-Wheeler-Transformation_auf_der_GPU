__global__ void Rank_Group_Has_Only_Stack_Symbols3(int* Rule_Stack_Index, int* Rank_Group, int* Prefix_Sum1, int rules_plus_stack, int size_Rules) {
    int thread_id = threadIdx.x + blockDim.x * blockIdx.x;
    if (thread_id < rules_plus_stack) {
