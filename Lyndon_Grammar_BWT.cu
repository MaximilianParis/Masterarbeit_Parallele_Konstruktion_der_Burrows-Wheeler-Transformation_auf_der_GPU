template<ull P, std::size_t J>
constexpr ull compute_power_entry() {
    ull cur = Base;
    for (int k = 0; k < J; ++k) {
        cur = ((ui128(cur) * cur) % P);
    }
    return (cur);
}

// --- build inner array for one prime P of length Bits ---
template<ull P, std::size_t Bits, std::size_t... J>
constexpr std::array<ull, Bits> compute_powers_impl(std::index_sequence<J...>) {
    // aggregate initialization: avoids operator[]
    return { compute_power_entry<P, J>()... };
}

template<ull P, std::size_t Bits>
constexpr std::array<ull, Bits> compute_powers() {
    return compute_powers_impl<P, Bits>(std::make_index_sequence<Bits>{});
}

