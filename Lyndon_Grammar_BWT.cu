        Prefix_Sum2[thread_id] = (Rank_Group[thread_id] < 0) ? val : 0;

    }
}



__global__ void Update_BWT_Offset_And_Insert_In_BWT(int* Rule_Stack_Index, int* Index_To_Right_Parent_L, int* Index_To_Right_Parent_R, unsigned char* Character_Key, int* BWT_Offset, int* Prefix_Sum1, int* Prefix_Sum2, int* Prefix_Sum3, int* Rank_Group, unsigned char* Dev_Output, int rules_plus_stack, int size_Rules) {
    int thread_id = threadIdx.x + blockDim.x * blockIdx.x;
    if (thread_id < rules_plus_stack) {

        if (Rank_Group[thread_id] < 0) {
            unsigned char rank2 = Character_Key[thread_id];
            int l = ((thread_id - 1 >= 0) ? Prefix_Sum1[thread_id - 1] : 0) + BWT_Offset[thread_id];
            int r = Prefix_Sum1[thread_id] + BWT_Offset[thread_id];

            for (int i = l; i < r; i++) {
                Dev_Output[i] = rank2;
            }
            Prefix_Sum3[thread_id] = 0;
        }
        else {
            BWT_Offset[thread_id] += ((thread_id - 1 >= 0) ? Prefix_Sum2[thread_id - 1] : 0);
            int rule_stack_index = Rule_Stack_Index[thread_id];
            assert(rule_stack_index != -1);
            Prefix_Sum3[thread_id] = (rule_stack_index < size_Rules) ? Index_To_Right_Parent_R[rule_stack_index] - Index_To_Right_Parent_L[rule_stack_index] : 1;

        }

    }
}






