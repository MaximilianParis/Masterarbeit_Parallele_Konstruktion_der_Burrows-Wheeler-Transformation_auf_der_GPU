__global__ void Compute_Size_right_Parent(int* Rule_Rigth, int* Rule_Left, int* Stack, unsigned int* Prefix_Sum, int size_Rules, int rules_plus_stack) {
    int thread_id = threadIdx.x + blockDim.x * blockIdx.x;
    if (thread_id < rules_plus_stack) {
        if (thread_id < size_Rules) {
            int cur_rule = Rule_Left[thread_id];
            if (cur_rule != -1) {
                int cur_next = Rule_Rigth[cur_rule];
                int cnt = 0;
                while (cur_next != -1) {
                    cnt++;
                    cur_rule = cur_next;
                    cur_next = Rule_Rigth[cur_rule];
                }

                Prefix_Sum[thread_id] = cnt;
            }
            else
                Prefix_Sum[thread_id] = 0;
        }
        else {
            int stack_index = thread_id - size_Rules;
            if (stack_index > 0) {
                int cur_rule = Stack[stack_index - 1];
                int cur_next = Rule_Rigth[cur_rule];
                int cnt = 0;

                while (cur_next != -1) {
                    cnt++;
                    cur_rule = cur_next;
                    cur_next = Rule_Rigth[cur_rule];
                }


                if (thread_id == rules_plus_stack - 1) {
                    int cur_rule = Stack[stack_index];
                    int cur_next = Rule_Rigth[cur_rule];

                    while (cur_next != -1) {
                        cnt++;
                        cur_rule = cur_next;
                        cur_next = Rule_Rigth[cur_rule];
                    }

                }

                Prefix_Sum[thread_id] = cnt;
            }
            else {
                int cnt = 0;
                if (thread_id == rules_plus_stack - 1) {
                    int cur_rule = Stack[stack_index];
                    int cur_next = Rule_Rigth[cur_rule];

                    while (cur_next != -1) {
                        cnt++;
                        cur_rule = cur_next;
                        cur_next = Rule_Rigth[cur_rule];
                    }

                }

                Prefix_Sum[thread_id] = cnt;
            }

        }
    }
}

__global__ void Compute_right_Parent(int* Rule_Rigth, int* Rule_Left, int* Stack, unsigned int* Prefix_Sum, int* Right_Parent_Key1, int* Right_Parent_Key2, int size_Rules, int rules_plus_stack) {
    int thread_id = threadIdx.x + blockDim.x * blockIdx.x;
    if (thread_id < rules_plus_stack) {
        if (thread_id < size_Rules) {
            int cur_rule = Rule_Left[thread_id];
            if (cur_rule != -1) {
                int cur_next = Rule_Rigth[cur_rule];
                int cnt = 0;
                int index = (thread_id - 1 >= 0) ? Prefix_Sum[thread_id - 1] : 0;
                while (cur_next != -1) {

                    Right_Parent_Key1[index + cnt] = thread_id;
                    Right_Parent_Key2[index + cnt] = cur_rule;
                    cnt++;
                    cur_rule = cur_next;
                    cur_next = Rule_Rigth[cur_rule];
                }


            }

        }
        else {
            int stack_index = thread_id - size_Rules;
            if (stack_index > 0) {
                int index = (thread_id - 1 >= 0) ? Prefix_Sum[thread_id - 1] : 0;
                int cnt = 0;

                int cur_rule = Stack[stack_index - 1];
                int cur_next = Rule_Rigth[cur_rule];
                while (cur_next != -1) {
                    Right_Parent_Key1[index + cnt] = thread_id;
                    Right_Parent_Key2[index + cnt] = cur_rule;
                    cnt++;
                    cur_rule = cur_next;
                    cur_next = Rule_Rigth[cur_rule];
                }

                if (thread_id == rules_plus_stack - 1) {
                    int cur_rule = Stack[stack_index];
                    int cur_next = Rule_Rigth[cur_rule];

                    while (cur_next != -1) {
                        Right_Parent_Key1[index + cnt] = -1;
                        Right_Parent_Key2[index + cnt] = cur_rule;
                        cnt++;
                        cur_rule = cur_next;
                        cur_next = Rule_Rigth[cur_rule];
                    }

                }

            }
            else if (thread_id == rules_plus_stack - 1) {
                int cur_rule = Stack[stack_index];
                int cur_next = Rule_Rigth[cur_rule];
                int cnt = 0;
                int index = (thread_id - 1 >= 0) ? Prefix_Sum[thread_id - 1] : 0;
                while (cur_next != -1) {
                    Right_Parent_Key1[index + cnt] = -1;
                    Right_Parent_Key2[index + cnt] = cur_rule;
                    cnt++;
                    cur_rule = cur_next;
                    cur_next = Rule_Rigth[cur_rule];
                }
            }

        }
    }

}



__global__ void Compute_Index_To_Right_Parent_L_And_R(int* Right_Parent_Key1, int* Right_Parent_Key2, int* Index_To_Right_Parent_L, int* Index_To_Right_Parent_R, int size_Right_Parent) {
    int thread_id = threadIdx.x + blockDim.x * blockIdx.x;
    if (thread_id < size_Right_Parent) {

        int child = Right_Parent_Key2[thread_id];

        if (thread_id == 0 || (thread_id > 0 && child != Right_Parent_Key2[thread_id - 1])) {
            Index_To_Right_Parent_L[child] = thread_id;
        }

        if (thread_id == size_Right_Parent - 1 || (thread_id < size_Right_Parent - 1 && child != Right_Parent_Key2[thread_id + 1])) {
            Index_To_Right_Parent_R[child] = thread_id + 1;
        }

    }

}

__global__ void Init_Rule_Stack_Index_Prefix_Sum(int* Rule_Left, unsigned int* Prefix_Sum, int rules_plus_stack, int size_Rules) {
    int thread_id = threadIdx.x + blockDim.x * blockIdx.x;
    if (thread_id < rules_plus_stack) {
        if (thread_id < size_Rules) {
            int rule_left = Rule_Left[thread_id];
            Prefix_Sum[thread_id] = (rule_left != -1) ? 1 : 0;
        }
        else
            Prefix_Sum[thread_id] = 1;
    }
}

__global__ void Init_Rule_Stack_Index_And_Rank_Group_And_BWT_Offset(int* Rule_Rigth, int* Rule_Lenght, int* Rule_Start, int* Rule_Stack_Index, int* Rank_Group, int* BWT_Offset, unsigned char* Character_Key, unsigned char* Dev_Input, int* Stack_Start, unsigned int* Prefix_Sum, int rules_plus_stack, int size_Rules, int n) {
    int thread_id = threadIdx.x + blockDim.x * blockIdx.x;
    if (thread_id < rules_plus_stack) {

        int index = Prefix_Sum[thread_id];
        if ((thread_id == 0 && index > 0) || (thread_id > 0 && index != Prefix_Sum[thread_id - 1])) {
            index--;
            Rule_Stack_Index[index] = thread_id;
            Rank_Group[index] = 0;
            BWT_Offset[index] = 0;
            int rule_stack_index = thread_id;
            if (rule_stack_index < size_Rules) {
                int right_rule = Rule_Rigth[rule_stack_index];
                int length1 = Rule_Lenght[rule_stack_index];
                int rule_start = Rule_Start[rule_stack_index];
                int length2 = Rule_Lenght[right_rule];
                Character_Key[index] = Dev_Input[rule_start + length1 - length2 - 1];

            }
            else {
                int rule_start = Stack_Start[rule_stack_index - size_Rules];
                Character_Key[index] = (rule_start - 1 >= 0) ? Dev_Input[rule_start - 1] : Dev_Input[n - 1];
            }
        }

    }

}



__global__ void Init_Key_For_BWT_Sort(int* Rule_Stack_Index, int* Rank_Group, unsigned long long* Key, int* Rule_Rigth, int* Rule_Rank, int* Stack, int rules_plus_stack, int size_Rules, int size_Stack, int n) {
    int thread_id = threadIdx.x + blockDim.x * blockIdx.x;
    if (thread_id < rules_plus_stack) {
        int rule_stack_index = Rule_Stack_Index[thread_id];
        if (rule_stack_index < size_Rules && rule_stack_index != -1) {

            int right_rule = Rule_Rigth[rule_stack_index];

            int val1 = Rule_Rank[right_rule] + 1;
            int val2 = Rank_Group[thread_id];

            Key[thread_id] = ((unsigned long long)val1) | (((unsigned long long)val2) << 32);

        }
        else if (rule_stack_index >= size_Rules)
        {
            int rule_index = Stack[rule_stack_index - size_Rules];

            int val2 = Rank_Group[thread_id];
            int val1;
            if (val2 < 0) {
                val1 = (size_Stack - (rule_stack_index - size_Rules)) + 1;
                val2 = -val2 - 1;
                Rank_Group[thread_id] = val2;
            }
            else
                val1 = Rule_Rank[rule_index] + 1;


            Key[thread_id] = ((unsigned long long)val1) | (((unsigned long long)val2) << 32);

        }
        else {
            int val2 = Rank_Group[thread_id];
            if (val2 < 0) {
                val2 = -val2 - 1;
                Rank_Group[thread_id] = val2;
            }

            Key[thread_id] = ((unsigned long long)0) | (((unsigned long long)val2) << 32);
        }
    }

}

